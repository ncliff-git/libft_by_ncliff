#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int		i;
	int		cpn;
	char	cn;

	i = 1;
	cpn = n;
	if (n == -2147483648)
		write(fd, "-2147483648", 11);
	if (n < 0 && n != -2147483648)
	{
		write(fd, "-", 1);
		cpn = n *= -1;
	}
	while (n > 9 && n != -2147483648 && i != 1000000000)
	{
		i *= 10;
		n /= 10;
	}
	while (i > 0 && n != -2147483648)
	{
		cn = (cpn / i) + '0';
		write(fd, &cn, 1);
		cpn %= i;
		i /= 10;
	}
}
