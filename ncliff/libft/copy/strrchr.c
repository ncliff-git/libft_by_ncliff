char	*ft_strrchr(const char *s, int c)
{
	int i;
	int lastc;

	i = 0;
	lastc = 0;
	while (s[i] != '\0')
	{
		if (s[i] == c)
			lastc = i;
		i++;
	}
	return ((char *)s + lastc);
}
