#include "libft.h"

void	ft_putstr_fd(char *s, int fd)
{
	int	n;
	int i;

	if (s == NULL)
		return ;
	n = ft_strlen(s);
	i = 0;
	while (i != n)
	{
		write(fd, &s[i], 1);
		++i;
	}
}
